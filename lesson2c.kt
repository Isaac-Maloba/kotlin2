fun main(args: Array<String>) {
    // if...else if...else conditional statement
    // This is used to evaluate mutiple conditions
    // The else part gets executed when all the conditionsgiven evaluate to false
    val number = 0
    if(number > 0) {
        println("The number is positive")
    }
    else if(number == 0) {
        println("The number is zero")
    }
    else {
        println("The number is negative")
    }
}