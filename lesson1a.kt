fun main(){
    // A variable is a container that is used to store data/ values
    // Variable naming rules
    //   1. Can only contain letters, numbers and underscores; no spaces or other symbols; cannot start with a number
    //   2. Cannot use Kotlin's reserved/ key words
    //   3. Have nemaing conventions: camelCase, CamelCase, camel_case
    // In Kotlin, we have two main types of initializing variables
    // val => Immutable (Cannot be changed) & var => Mutable (can be changed)

    val name = "Isaac Maloba" // A string => A series of characters inside of quotes
    val number1 = 5 // An integer => Whole number
    val number2 = 40.6 // A double => A number with a decimal
    val letter = "K" // A char(character) => A single letter of the alphabet
    val isRegistered = true // A boolean => EEvaluates to either true or false

    println(name)
    println(number1)
    println(number2)
    println(letter)
    println(isRegistered)
}