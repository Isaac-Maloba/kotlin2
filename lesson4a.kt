fun main() {
    // They are blocks of statements that perform a given task
    // Functions are introduced by the 'fun' keyword followed by the name of the function, parenthesis and finally the function body which is introduced by the curly braces
    fun sayHello () {
        println("Hello there! Hope you are having a wonderful day!!")
    }
    // Below we invoke/call our function
    sayHello()
    println("<========================>")
    // Create your own function and invoke it
    fun question () {
        println("Where will the event be held?")
    }
    // Invoke the function
    question()
}